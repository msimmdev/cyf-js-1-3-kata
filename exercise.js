// Complete the filterNumberArray function below
// The function takes 3 parameters
// numberList = an array
// lowerLimit = an integer
// upperLimit = an integer
// 
// RULES
// 1. The function should return an array of integers
// 2. The returned array should include all numbers in numberList that are
// between lowerLimit and upperLimit (inclusive) in the same order as numberList
// 3. If an item in numberList is not an integer it should not be included in the output
// 4. If lowerLimit is greater that upperLimit the function should return
// the unaltered numberList
//
// EXAMPLE
// filterNumberArray([7,4,1,3], 2, 4)
// should return
// [4,3]
// as 4 and 3 are the only provided numbers between 2 and 4 (inclusive)

function filterNumberArray(numberList, lowerLimit, upperLimit) {

    if (lowerLimit > upperLimit) {
        return numberList;
    }

    let returnArray = [];
    numberList.forEach(num => {
        if (Number.isInteger(num)) {
            if (num >= lowerLimit && num <= upperLimit) {
                returnArray.push(num);
            }
        }
    });
    return returnArray;
}

module.exports.filterNumberArray = filterNumberArray;